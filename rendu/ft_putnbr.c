#include "libft.h"

void	ft_putnbr(int n)
{
	char	nbr[10];
	int		i;

	if (n == -2147483648)
	{
		ft_putstr("-2147483648");
		return ;
	}
	if (n < 0)
	{
		ft_putchar('-');
		n *= -1;
	}
	i = 10;
	while (n >= 0)
	{
		nbr[--i] = n % 10;
		n = (n - (n % 10)) / 10;
		if (n == 0)
			break ;
	}
	while (i < 10)
		ft_putchar(nbr[++i - 1] + 48);
	return ;
}
